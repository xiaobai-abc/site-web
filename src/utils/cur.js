export function CursorStyle() {
  window.onclick = function (event) {
    const len = list.length;
    const index = random(0, len - 1);
    const heart = document.createElement("b"); //创建b元素
    heart.onselectstart = new Function("event.returnValue=false"); //防止拖动
    document.body.appendChild(heart).innerHTML = list[index]; //将b元素添加到页面上
    const styles = {
      left: event.clientX + "px",
      top: event.clientY + "px",
      position: "fixed",
      "z-index": 9999,
      cursor: "default",
      pointerEvents: "none"
    };
    Object.assign(heart.style, styles);
    heart.addEventListener("animationend", animationEnd);
    heart.classList.add("fedaTopAnimation");
    function animationEnd(event) {
      event.target.removeEventListener("animationend", animationEnd);
      event.target.remove();
    }
  };
}
